package com.ihsm.university.ihsmtestcases.pojo;

public class StudentVisaOnlineData {
	private String visaType;
	private String startDate;
	private String endDate;
	private String expiryDate;
	private String onlineVisaNumber;

	public String getVisaType() {
		return visaType;
	}

	public void setVisaType(String visaType) {
		this.visaType = visaType;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getExpiryDate() {
		return expiryDate;
	}

	public void setExpiryDate(String expiryDate) {
		this.expiryDate = expiryDate;
	}

	public String getOnlineVisaNumber() {
		return onlineVisaNumber;
	}

	public void setOnlineVisaNumber(String onlineVisaNumber) {
		this.onlineVisaNumber = onlineVisaNumber;
	}
}
