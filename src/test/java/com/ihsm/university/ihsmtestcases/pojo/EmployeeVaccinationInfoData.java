package com.ihsm.university.ihsmtestcases.pojo;

public class EmployeeVaccinationInfoData {

	private String vaccineName;
	private String doseCount;
	private String batchNumber;
	private String vaccineExpiry;
	private String notes;

	public String getVaccineName() {
		return vaccineName;
	}

	public void setVaccineName(String vaccineName) {
		this.vaccineName = vaccineName;
	}

	public String getDoseCount() {
		return doseCount;
	}

	public void setDoseCount(String doseCount) {
		this.doseCount = doseCount;
	}

	public String getBatchNumber() {
		return batchNumber;
	}

	public void setBatchNumber(String batchNumber) {
		this.batchNumber = batchNumber;
	}

	public String getVaccineExpiry() {
		return vaccineExpiry;
	}

	public void setVaccineExpiry(String vaccineExpiry) {
		this.vaccineExpiry = vaccineExpiry;
	}

	public String getNotes() {
		return notes;
	}

	public void setNotes(String notes) {
		this.notes = notes;
	}
}
