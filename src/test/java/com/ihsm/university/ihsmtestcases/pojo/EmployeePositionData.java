package com.ihsm.university.ihsmtestcases.pojo;

public class EmployeePositionData {

	private String experienceType;
	private String fromDate;
	private String toDate;
	private String universityName;
	private String universityPosition;
	private String notes;

	public String getExperienceType() {
		return experienceType;
	}

	public void setExperienceType(String experienceType) {
		this.experienceType = experienceType;
	}

	public String getFromDate() {
		return fromDate;
	}

	public void setFromDate(String fromDate) {
		this.fromDate = fromDate;
	}

	public String getToDate() {
		return toDate;
	}

	public void setToDate(String toDate) {
		this.toDate = toDate;
	}

	public String getUniversityName() {
		return universityName;
	}

	public void setUniversityName(String universityName) {
		this.universityName = universityName;
	}

	public String getUniversityPosition() {
		return universityPosition;
	}

	public void setUniversityPosition(String universityPosition) {
		this.universityPosition = universityPosition;
	}

	public String getNotes() {
		return notes;
	}

	public void setNotes(String notes) {
		this.notes = notes;
	}
}
