package com.ihsm.university.ihsmtestcases.pojo;

public class EmployeeGuardianInfoData {

	private String guardianType;
	private String guardianName;
	private String guardianDob;
	private String emergencyContact;

	public String getGuardianType() {
		return guardianType;
	}

	public void setGuardianType(String guardianType) {
		this.guardianType = guardianType;
	}

	public String getGuardianName() {
		return guardianName;
	}

	public void setGuardianName(String guardianName) {
		this.guardianName = guardianName;
	}

	public String getGuardianDob() {
		return guardianDob;
	}

	public void setGuardianDob(String guardianDob) {
		this.guardianDob = guardianDob;
	}

	public String getEmergencyContact() {
		return emergencyContact;
	}

	public void setEmergencyContact(String emergencyContact) {
		this.emergencyContact = emergencyContact;
	}
}
